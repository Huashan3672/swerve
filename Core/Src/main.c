float encoder_read_rpm(void)
{
    uint16_t enc_now;
    int16_t diff;

    enc_now = (uint16_t)__HAL_TIM_GET_COUNTER(&htim3);
    diff = (int16_t)(enc_now - enc_last);
    enc_last = enc_now;

    motor_rpm = ((float)diff * 60.0f) / (0.1f * 720.0f);

    return motor_rpm;
}

void pid_calc(PID_t* pid)
   	  {
   	      pid->err = pid->target - pid->current;

   	      pid->p_out = pid->Kp * pid->err;
   	      pid->i_out += pid->Ki * pid->err* TS_SEC;

   	      //绌嶏拷?锟斤拷?锟斤拷??
   	      limit(&(pid->i_out), pid->IntegralLimit);



   	      pid->d_out = pid->Kd * ((pid->err - pid->prev_err)/TS_SEC);

   	      pid->total_out = pid->p_out + pid->i_out + pid->d_out;
   	      //pid杈撳嚭??锟斤拷??
   	      limit(&(pid->total_out), pid->MaxOutput);

   	      pid->prev_err = pid->err;
   	  }


